public class Add{
	public static void main(String[] args){
		
		float a = 2.5f;
		float b = 4.13f;
		float suma;
		suma = a + b;
		
		System.out.println("The addition of 2.5 and 4.13 is "+ suma);
		
		
		float c = -33.6576f;
		float d = -21.3335f;
		float sumb;
		sumb = c + d;
		
		System.out.println("The addition of -33.6576 and -21.3335 is "+ sumb);
		
		
		
		float x = 21.5f;
		float y = 1.2E2f;
		float sumc;
		sumc = x + y;
		
		System.out.println("The addition of 21.5 and 1.2E2 is "+ sumc);
		
		
		
		float p = 0.0007f;
		float q = 1.2e-04f;
		float sumd;
		sumd = p + q;
		
		System.out.println("The addition of 0.0007 and 1.2e-04 is "+ sumd);
	}
}